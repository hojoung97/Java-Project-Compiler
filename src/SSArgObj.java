public class SSArgObj extends ArgObj {
    private String str1;
    private String str2;

    public SSArgObj(String newStr1, String newStr2) {
        str1 = newStr1;
        str2 = newStr2;
    }

    public String getString1() {
        return str1;
    }
    public String getString2() {
        return str2;
    }
}
